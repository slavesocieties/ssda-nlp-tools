def load_transcription_json(json_file_path: Path) -> List[Dict[str, Any]]:
    '''
    Loading Json and verify the top-level object is list
    '''
    with open(json_file_path, "r", encoding="utf-8") as f:
        data = json.load(f)
    if not isinstance(data, list):
        raise ValueError("Expected the input JSON to be a list of items.")
    return data


def infer_volume_id(data: List[Dict[str, Any]], fallback: Optional[int] = None) -> int:
    '''
    determine the volume/book ID, using a manual override if provided
    '''

    if fallback is not None:
        return fallback
    
    for item in data:
        metadata = item.get("metadata", {})
        identifier = metadata.get("identifier")
        if identifier is not None:
            try:
                return int(identifier)
            except (TypeError, ValueError):
                pass

        for image in item.get("images", []):
            file_name = image.get("file", "")
            match = re.match(r"(\d+)-\d+\.jpg$", file_name)
            if match:
                return int(match.group(1))

    raise ValueError("Could not infer volume id from the transcription JSON.")


def infer_title(data: List[Dict[str, Any]], fallback: Optional[str] = None) -> str:
    '''
    determine the title ID, using a manual override if provided
    '''
    
    if fallback:
        return fallback

    for item in data:
        metadata = item.get("metadata", {})
        titles = metadata.get("title")
        if isinstance(titles, list) and titles:
            return str(titles[0])
        if isinstance(titles, str) and titles.strip():
            return titles.strip()

        direct_title = item.get("title")
        if isinstance(direct_title, list) and direct_title:
            return str(direct_title[0])
        if isinstance(direct_title, str) and direct_title.strip():
            return direct_title.strip()

    return "Untitled volume"


def clean_text(text: str) -> str:
    '''
    text cleaning
    '''
    text = text.replace("\r", "")
    lines = [line.strip() for line in text.splitlines() if line.strip()]
    merged: List[str] = []

    for line in lines:
        if not merged:
            merged.append(line)
        elif merged[-1].endswith("-"):
            merged[-1] = merged[-1][:-1] + line
        else:
            merged.append(line)

    return re.sub(r"\s+", " ", " ".join(merged)).strip()


def should_keep_prelude(prelude: str) -> bool:
    '''
    Keep text before first 'real' entry only if satisfy conditions(15 words or 100 char)
    Most of the case it will not keep anything
    '''
    words = prelude.split()
    return len(words) >= 15 or len(prelude) >= 100


def strip_trailing_marginalia(lines: List[str]) -> List[str]:
    '''
    Removed junk text at the end
    '''
    cleaned = [line.rstrip() for line in lines]
    while cleaned and not cleaned[-1].strip():
        cleaned.pop()

    short_run: List[str] = []
    idx = len(cleaned) - 1
    while idx >= 0 and cleaned[idx].strip() and len(cleaned[idx].strip()) <= 30:
        short_run.append(cleaned[idx].strip())
        idx -= 1

    if len(short_run) >= 2:
        return cleaned[: idx + 1]

    if len(short_run) == 1:
        candidate = short_run[0]
        if re.match(r"^\d+[A-Za-z\.]?\s*$", candidate) or re.match(r"^\d+[A-Za-z\.]?\s+.+", candidate):
            return cleaned[: idx + 1]

    return cleaned

